const theory = `## 1. Идея алгоритма
Selection Sort на каждом шаге ищет минимальный элемент в неотсортированной части массива и ставит его на следующую правильную позицию. Алгоритм сначала полностью выбирает лучший кандидат, а затем делает обмен.

## 2. Как алгоритм работает пошагово
1. На первом проходе ищется минимум во всём массиве.
2. Найденный минимум меняется местами с элементом в позиции 0.
3. Затем поиск минимума повторяется для диапазона от 1 до конца.
4. После каждого прохода левая часть увеличивается и считается отсортированной.

## 3. Маленький пример
Для массива [7, 4, 5, 2]:
- Минимум первого прохода — 2, после обмена получаем [2, 4, 5, 7]
- На втором проходе рассматривается подмассив [4, 5, 7], минимум уже стоит правильно.

## 4. Почему алгоритм ведёт себя именно так
Selection Sort отделяет поиск от перестановки. Он почти всегда делает одинаковое число сравнений, потому что обязан просмотреть весь хвост, чтобы убедиться в минимуме.

## 5. Анализ сложности
Лучший, средний и худший случаи по времени остаются квадратичными. Дополнительная память практически не нужна.

## 6. Свойства
- Классическая версия нестабильна.
- Алгоритм работает in-place.
- Алгоритм неадаптивен.
- Структура итеративная.

## 7. Преимущества
Алгоритм прост для анализа и делает небольшое число обменов. Это полезно для учебной демонстрации идеи выбора лучшего кандидата.

## 8. Недостатки
Selection Sort выполняет много сравнений и не ускоряется на почти отсортированных данных. В практических задачах его обычно заменяют более эффективные методы.

## 9. Когда применять уместно
Алгоритм уместен в учебных целях и для очень маленьких массивов, когда число обменов важнее общего времени.

## 10. Когда применять не стоит
Не стоит применять Selection Sort, если важна стабильность, скорость или адаптация к частично отсортированному входу.

## 11. Типичные ошибки и заблуждения
- Выполнять обмен при каждом нахождении нового минимума вместо одного обмена после завершения прохода.
- Считать, что малое число обменов автоматически делает алгоритм быстрым.

## 12. Сравнение с другими алгоритмами платформы
Selection Sort делает меньше обменов, чем Bubble Sort, но проигрывает ему по стабильности. Insertion Sort лучше работает на почти отсортированных массивах, а Merge Sort и Quick Sort намного эффективнее на больших входах.`;


const theory = `## 1. Идея алгоритма
Bubble Sort, или сортировка пузырьком, многократно проходит по массиву и сравнивает соседние элементы. Если элементы стоят в неверном порядке, алгоритм меняет их местами. После каждого полного прохода самый большой элемент из неотсортированной части "всплывает" в конец массива.

## 2. Как алгоритм работает пошагово
1. Сравниваются элементы с индексами 0 и 1.
2. Если левый элемент больше правого, выполняется обмен.
3. Затем сравниваются элементы 1 и 2, 2 и 3 и так далее.
4. После первого прохода последний элемент гарантированно стоит правильно.
5. Граница неотсортированной части сокращается, и процесс повторяется.
6. Если на проходе не было обменов, алгоритм завершается досрочно.

## 3. Маленький пример
Для массива [5, 3, 4, 1]:
- 5 и 3 меняются местами: [3, 5, 4, 1]
- 5 и 4 меняются местами: [3, 4, 5, 1]
- 5 и 1 меняются местами: [3, 4, 1, 5]
После первого прохода число 5 уже стоит на итоговой позиции.

## 4. Почему алгоритм ведёт себя именно так
Bubble Sort делает только локальные исправления порядка. Крупные элементы постепенно двигаются вправо через цепочку обменов, поэтому на больших массивах число операций быстро растёт.

## 5. Анализ сложности
Без оптимизации алгоритм всегда делает много сравнений. С флагом обменов лучший случай возникает на уже отсортированном массиве, когда один проход подтверждает отсутствие нарушений.

## 6. Свойства
- Стабилен при обмене только по условию A[j] > A[j + 1].
- Работает in-place.
- Частично адаптивен в версии с досрочным завершением.
- Имеет итеративную структуру.

## 7. Преимущества
Bubble Sort легко объяснить и визуализировать. Он хорошо показывает идею локальных сравнений и обменов, поэтому полезен как первый учебный алгоритм сортировки.

## 8. Недостатки
Главный недостаток — низкая производительность. Для массивов среднего и большого размера он сильно уступает более эффективным подходам.

## 9. Когда применять уместно
Алгоритм уместен в учебных целях и для очень маленьких наборов данных, когда важнее простота, чем производительность.

## 10. Когда применять не стоит
Не стоит использовать Bubble Sort для больших массивов и производственных задач, где важны скорость и масштабируемость.

## 11. Типичные ошибки и заблуждения
- Считать, что после одного обмена массив становится почти отсортированным.
- Забывать сократить правую границу прохода.
- Не использовать флаг обменов и терять лучший случай O(n).

## 12. Сравнение с другими алгоритмами платформы
Bubble Sort делает больше обменов, чем Selection Sort, и хуже использует частичную отсортированность, чем Insertion Sort. По сравнению с Merge Sort и Quick Sort он намного медленнее на больших данных.`;


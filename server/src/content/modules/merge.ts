const theory = `## 1. Идея алгоритма
Merge Sort использует стратегию "разделяй и властвуй". Массив рекурсивно делится на две половины до тех пор, пока не останутся подмассивы длины 1. Затем эти подмассивы последовательно сливаются обратно в отсортированном порядке.

## 2. Как алгоритм работает пошагово
1. Массив делится примерно пополам.
2. Левая половина сортируется рекурсивно.
3. Правая половина сортируется рекурсивно.
4. Две отсортированные части сливаются линейно.
5. Процесс повторяется до восстановления всего массива.

## 3. Маленький пример
Для массива [6, 3, 8, 2]:
- Деление даёт [6, 3] и [8, 2]
- Затем [6, 3] превращается в [3, 6], а [8, 2] — в [2, 8]
- При слиянии сравниваются 3 и 2, затем 3 и 8, затем 6 и 8
Результат: [2, 3, 6, 8]

## 4. Почему алгоритм ведёт себя именно так
Главная идея эффективности Merge Sort в том, что слияние двух уже отсортированных частей можно выполнить линейно. Высота дерева рекурсии порядка log n, а работа на каждом уровне суммарно линейна.

## 5. Анализ сложности
В лучшем, среднем и худшем случаях сложность остаётся O(n log n). Дополнительная память нужна для временных массивов во время слияния.

## 6. Свойства
- Алгоритм стабилен при корректном merge.
- Классическая версия не является in-place.
- Обычно не считается адаптивным.
- Имеет рекурсивную структуру.

## 7. Преимущества
Merge Sort гарантирует предсказуемую производительность, хорошо подходит для больших наборов данных и сохраняет стабильность.

## 8. Недостатки
Основной недостаток — дополнительная память. Кроме того, реализация и визуализация сложнее, чем у простых квадратичных алгоритмов.

## 9. Когда применять уместно
Алгоритм уместен, когда нужна гарантированная сложность O(n log n), стабильность и понятная стратегия divide-and-conquer.

## 10. Когда применять не стоит
Не стоит выбирать Merge Sort, если критична экономия памяти и сортировка должна быть строго in-place.

## 11. Типичные ошибки и заблуждения
- Считать, что merge — это простое склеивание без сравнений.
- Полагать, что рекурсия сама по себе обеспечивает скорость, хотя главное здесь — линейное слияние на каждом уровне.

## 12. Сравнение с другими алгоритмами платформы
Merge Sort намного быстрее Bubble, Selection и Insertion на больших входах. В отличие от Quick Sort он гарантирует O(n log n) даже в худшем случае и остаётся стабильным, но проигрывает по памяти и часто по константным факторам.`;


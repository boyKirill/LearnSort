const theory = `## 1. Идея алгоритма
Insertion Sort строит отсортированную часть массива слева направо. Каждый новый элемент берётся из неотсортированной части и вставляется в правильное место внутри уже отсортированного префикса.

## 2. Как алгоритм работает пошагово
1. Первый элемент считается отсортированным.
2. Следующий элемент берётся как ключ.
3. Пока элементы слева больше ключа, они сдвигаются вправо.
4. Ключ записывается в освободившуюся позицию.
5. Отсортированный префикс увеличивается на один элемент.

## 3. Маленький пример
Для массива [4, 2, 5, 3]:
- Ключ 2 сравнивается с 4, 4 сдвигается вправо, 2 вставляется в начало.
- Ключ 5 уже не меньше 4, поэтому остаётся на месте.
- Ключ 3 сдвигает 5 и 4 вправо, затем вставляется между 2 и 4.

## 4. Почему алгоритм ведёт себя именно так
Insertion Sort поддерживает инвариант: левая часть уже отсортирована. Поэтому, если новый элемент почти на своём месте, работы требуется мало.

## 5. Анализ сложности
Лучший случай наблюдается на уже отсортированном массиве, худший — на обратно отсортированном, когда каждый новый ключ продвигается в начало.

## 6. Свойства
- Алгоритм стабилен.
- Работает in-place.
- Адаптивен.
- Обычно реализуется итеративно.

## 7. Преимущества
Insertion Sort очень хорош для маленьких и почти отсортированных массивов. Он часто используется как вспомогательный алгоритм внутри более сложных гибридных решений.

## 8. Недостатки
На больших и хаотичных входах алгоритм остаётся квадратичным и уступает O(n log n)-сортировкам.

## 9. Когда применять уместно
Алгоритм уместен для коротких списков, частично отсортированных данных и встроенных оптимизаций внутри гибридных сортировок.

## 10. Когда применять не стоит
Не стоит использовать его как основной алгоритм для больших случайных массивов.

## 11. Типичные ошибки и заблуждения
- Путать сдвиги элементов с серией обменов.
- Недооценивать выигрыш на почти отсортированных входах.

## 12. Сравнение с другими алгоритмами платформы
Insertion Sort обычно лучше Bubble Sort и Selection Sort на почти отсортированных массивах. Merge Sort быстрее на больших данных и стабилен, а Quick Sort часто быстрее в среднем, но нестабилен.`;


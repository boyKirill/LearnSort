const theory = `## 1. Идея алгоритма
Quick Sort выбирает опорный элемент (pivot), перестраивает массив так, чтобы элементы меньше опорного оказались слева, а больше — справа, после чего рекурсивно сортирует получившиеся части.

## 2. Как алгоритм работает пошагово
1. Выбирается опорный элемент.
2. Остальные элементы сравниваются с опорным.
3. Меньшие значения отправляются в левую часть, большие — в правую.
4. Pivot занимает окончательную позицию.
5. Левая и правая части сортируются рекурсивно.

## 3. Маленький пример
Для массива [5, 2, 7, 3]:
- В качестве pivot можно взять 3
- После разбиения pivot оказывается на своей позиции
- Далее рекурсивно сортируются подмассивы слева и справа

## 4. Почему алгоритм ведёт себя именно так
После удачного разбиения задача резко уменьшается: pivot сразу попадает на итоговую позицию. Если разбиения получаются примерно равными, дерево рекурсии оказывается сбалансированным.

## 5. Анализ сложности
Средний случай — O(n log n). Худший O(n^2) возникает при крайне неудачных выборах pivot. Дополнительная память обычно связана со стеком рекурсии.

## 6. Свойства
- Классический Quick Sort нестабилен.
- Почти работает in-place.
- Обычно не считается адаптивным.
- Имеет рекурсивную структуру.

## 7. Преимущества
Quick Sort очень быстр на практике, часто имеет хорошие константные факторы и широко используется в различных модификациях.

## 8. Недостатки
Без аккуратного выбора опорного элемента возможен худший квадратичный случай. Алгоритм нестабилен и сложнее для пошагового понимания, чем простые сортировки.

## 9. Когда применять уместно
Алгоритм уместен для быстрой сортировки массивов в памяти, когда допустима нестабильность и важно хорошее среднее время.

## 10. Когда применять не стоит
Не стоит выбирать классический Quick Sort, если нужны стабильность или гарантированное O(n log n) в худшем случае без дополнительных улучшений.

## 11. Типичные ошибки и заблуждения
- Считать, что pivot обязательно должен быть медианой.
- Думать, что после одного разбиения массив почти отсортирован без дальнейшей рекурсии.

## 12. Сравнение с другими алгоритмами платформы
Quick Sort обычно быстрее Merge Sort по константам на массивах в памяти, но проигрывает ему по стабильности и гарантии худшего случая. Он значительно эффективнее Bubble, Selection и Insertion на больших случайных входах.`;

